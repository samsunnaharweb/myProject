	  <h5>At recent events with Diabetes Canada (the Black Expo and Durham Symposium), you’ve prepared dishes inspired by Jamaican Hakka cuisine. Can you tell me about this cuisine, its flavours, and how you came to learn about it? 
      I learned about it a couple years ago because we had a big dinner we were doing where Jamaican-Hakka cuisine was the theme. It’s a fascinating cuisine...during the mid-late 1800’s, a lot of Hakka Chinese immigrants moved to Jamaica for various reasons, and they combined the dishes and ingredients they encountered with their cooking techniques and flavours. </h5>
	  
