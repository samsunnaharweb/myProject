	  <h3>I grew up in the restaurant industry; my mum, Carrie, opened her first resto (in Kingston, ON), when I was 1.  I fell in love with food and cooking early, and started training when I was 11. When I was 15, we decided I was ready to take over the kitchen. I ran it for 8 years, during which time press from around the world covered it (Financial Times, CTV National News, Toronto Star). After 8 years we wanted to do something new, so we decided to sell the resto, move to Toronto, and start up L.U.S.T (Luke’s Underground Supper Club). We just passed our 3rd anniversary, and the Food Network named it one of the top 15 supper clubs in the world.</h3>
	  
